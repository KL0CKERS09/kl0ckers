const otherMenu = document.getElementById('otherMenu');
const otherMenuBody = document.getElementById('otherMenuBody');
const otherMenuBack = document.getElementById('other-menu-back');



otherMenu.addEventListener("click", () => {
    otherMenuBody.style.transform = "translateX(0em)"
    otherMenuBody.style.visibility = "visible"
    otherMenu.style.transition = "400ms"
    otherMenu.style.opacity = "0"
})

otherMenuBack.addEventListener("click", () => {
    otherMenuBody.style.transform = "translateX(-100em)"
    otherMenuBody.style.visibility = "hidden"
    otherMenu.style.transition = "700ms"
    otherMenu.style.opacity = "1"
})















































































































































